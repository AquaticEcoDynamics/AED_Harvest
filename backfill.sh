leap_year() {
  leap=$1
  if [ `expr $leap % 400` -eq 0 ] ; then
    echo 'Y'
  elif [ `expr $leap % 100` -eq 0 ] ; then
    echo 'N'
  elif [ `expr $leap % 4` -eq 0 ] ; then
    echo 'Y'
  else
    echo 'N'
  fi
}
